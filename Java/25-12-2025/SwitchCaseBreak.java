package com.dec25122025;

public class SwitchCaseBreak {

	public static void main(String[] args) {
		
		int num = 2;
		
		switch(num)
		{
		case 1: System.out.println("English");
		break;
		
		case 2: System.out.println("Hindi");
		break;
		
		case 3: System.out.println("Gujarati");
		break;
		}
	}
}
